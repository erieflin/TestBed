package warframe.json.templates;

public class ConclaveChallenge {
	private String id;
	private String description;
	private String expiry;
	private String amount;
	private String mode;
	private String category;
	private String eta;
	private boolean expired;
	private boolean daily;
	private boolean rootChallenge;
	private String endString;
	private String asString;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getExpiry() {
		return expiry;
	}
	public void setExpiry(String expiry) {
		this.expiry = expiry;
	}
	public String getAmount() {
		return amount;
	}
	public void setAmount(String amount) {
		this.amount = amount;
	}
	public String getMode() {
		return mode;
	}
	public void setMode(String mode) {
		this.mode = mode;
	}
	public String getCategory() {
		return category;
	}
	public void setCategory(String category) {
		this.category = category;
	}
	public String getEta() {
		return eta;
	}
	public void setEta(String eta) {
		this.eta = eta;
	}
	public boolean isExpired() {
		return expired;
	}
	public void setExpired(boolean expired) {
		this.expired = expired;
	}
	public boolean isDaily() {
		return daily;
	}
	public void setDaily(boolean daily) {
		this.daily = daily;
	}
	public boolean isRootChallenge() {
		return rootChallenge;
	}
	public void setRootChallenge(boolean rootChallenge) {
		this.rootChallenge = rootChallenge;
	}
	public String getEndString() {
		return endString;
	}
	public void setEndString(String endString) {
		this.endString = endString;
	}
	public String getAsString() {
		return asString;
	}
	public void setAsString(String asString) {
		this.asString = asString;
	}
	
}
